    private void rfBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rfBtnActionPerformed
        
        
        
        try{
            String value1=adm.getText();
            
            String sql="select adm_no, UID, CLASS, NAME, DATE_OF_BIRTH, CONTACT, DATE_OF_ADM, HSID, STATUS from StuDetail where UID='"+value1+"'  ";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            jTable_Stu.setModel(DbUtils.resultSetToTableModel(rs));
            
            
        }catch(Exception e)
        {
             JOptionPane.showMessageDialog(null,e);
        }
        
        
    }//GEN-LAST:event_rfBtnActionPerformed

