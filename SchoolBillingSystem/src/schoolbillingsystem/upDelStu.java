    private void jTable_StuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_StuMouseClicked
        
        
        
try{
            
            int row=jTable_Stu.getSelectedRow();
            String Table_click=(jTable_Stu.getModel().getValueAt(row, 0).toString());
            
            String sql = "select * from StuDetail where adm_no='"+Table_click+"' ";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                
                String add1=rs.getString("adm_no");
                uid.setText(add1);
                
                String add2=rs.getString("UID");
                adm.setText(add2);
                
                
                String add3=rs.getString("NAME");
                name.setText(add3);
                
                String add4=rs.getString("CLASS");
                cls.setText(add4);
                
                String add5=rs.getString("DATE_OF_BIRTH");
                dob.setText(add5);
                
                String add6=rs.getString("CONTACT");
                mob.setText(add6);
                
                String add7=rs.getString("DATE_OF_ADM");
                doa.setText(add7);
                
                String add8=rs.getString("HSID");
                hsid.setText(add8);
                
                String add9=rs.getString("STATUS");
                status.setText(add9);
            }
            
            
            
            
        }catch(Exception e){
            
             JOptionPane.showMessageDialog(null, "e");
            
        }

        
        
        
    }//GEN-LAST:event_jTable_StuMouseClicked

